                <h3>Privacy Policy</h3>
                <p>LearnTrack Pro processes school information for administration, results processing, academic reporting, attendance, announcements, and related operations. This policy describes the general privacy approach for the platform.</p>

                <h5>1. Information processed</h5>
                <p>The system may process school details, teacher records, student records, class and subject allocations, marks, attendance, announcements, report branding assets, and contact information used for school communication.</p>

                <h5>2. Purpose of processing</h5>
                <p>Information is processed only to support academic administration, report generation, communication, analytics, and school management activities authorized by the school.</p>

                <h5>3. Access control</h5>
                <p>LearnTrack Pro is designed to support role-based access. Schools are responsible for assigning user accounts properly and protecting credentials to reduce unauthorized access.</p>

                <h5>4. Data accuracy</h5>
                <p>Schools are responsible for ensuring information entered into the system is correct, lawful, and current. Users should review records and reports before official release.</p>

                <h5>5. Uploaded files and branding assets</h5>
                <p>Logos, signatures, and report card identity details uploaded by the school are stored for system operation and document generation. The school is responsible for ensuring it has the right to use those assets.</p>

                <h5>6. Communication data</h5>
                <p>Where announcements or email notifications are used, the school is responsible for the content it sends and for maintaining accurate recipient information.</p>

                <h5>7. Data retention</h5>
                <p>Data remains within the school system environment until updated, deleted, exported, or otherwise managed by the school or its authorized administrator.</p>

                <h5>8. Security</h5>
                <p>Reasonable system and account controls should be used, including strong passwords, restricted user access, secure hosting, database backups, and controlled administrator rights.</p>

                <h5>9. Changes to this policy</h5>
                <p>This privacy policy may be updated when the platform changes, when new features are introduced, or when stronger operational protections are needed.</p>

